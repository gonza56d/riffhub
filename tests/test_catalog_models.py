# GuitarModel.scale_display (single value vs multiscale range)
# ---------------------------------------------------------------------------
class GuitarScaleDisplayTests(TestCase):
    """scale_display trims trailing zeros and renders a single value (min==max)
    or an en-dash range (min!=max)."""

    @classmethod
    def setUpTestData(cls):
        cls.brand = Brand.objects.create(
            name="Display Brand", status=PublicationStatus.PUBLISHED
        )

    def _guitar(self, lo, hi, *, num_strings=6):
        return GuitarModel(
            brand=self.brand,
            name="Test",
            num_strings=num_strings,
            scale_length_min_inches=Decimal(lo),
            scale_length_max_inches=Decimal(hi),
        )

    def test_single_scale_whole_number_trims_all_zeros(self):
        # 25.000 -> '25"'
        guitar = self._guitar("25.000", "25.000")
        self.assertEqual(guitar.scale_display, '25"')

    def test_single_scale_one_decimal(self):
        # 25.500 -> '25.5"'
        guitar = self._guitar("25.500", "25.500")
        self.assertEqual(guitar.scale_display, '25.5"')

    def test_single_scale_two_decimals(self):
        # 24.750 -> '24.75"'  (the famous Gibson scale)
        guitar = self._guitar("24.750", "24.750")
        self.assertEqual(guitar.scale_display, '24.75"')

    def test_single_scale_no_trailing_dot(self):
        # The trim must never leave a dangling decimal point.
        guitar = self._guitar("25.000", "25.000")
        self.assertNotIn(".", guitar.scale_display)
        self.assertFalse(guitar.scale_display.rstrip('"').endswith("."))

    def test_multiscale_renders_endash_range(self):
        # min != max -> 'lo–hi"' using an EN DASH (U+2013), not a hyphen.
        guitar = self._guitar("25.500", "27.000", num_strings=7)
        self.assertEqual(guitar.scale_display, '25.5–27"')
        self.assertIn("–", guitar.scale_display)

    def test_multiscale_both_endpoints_trimmed_independently(self):
        # 25.500 -> '25.5', 26.500 -> '26.5'
        guitar = self._guitar("25.500", "26.500", num_strings=8)
        self.assertEqual(guitar.scale_display, '25.5–26.5"')

    def test_multiscale_mixed_precision_endpoints(self):
        # lo trims to whole, hi keeps two decimals.
        guitar = self._guitar("25.000", "26.750", num_strings=7)
        self.assertEqual(guitar.scale_display, '25–26.75"')

    def test_multiscale_uses_endash_not_hyphen(self):
        # Explicitly guard against a plain ASCII hyphen sneaking in.
        guitar = self._guitar("24.750", "25.500", num_strings=6)
        self.assertNotIn("-", guitar.scale_display)

    def test_equal_min_max_with_decimals_is_single_value(self):
        # Numerically-equal endpoints (with explicit places) collapse to single.
        guitar = self._guitar("24.750", "24.750")
        self.assertEqual(guitar.scale_display, '24.75"')
        self.assertNotIn("–", guitar.scale_display)

    def test_scale_display_always_ends_with_quote(self):
        for lo, hi in [("25.000", "25.000"), ("25.500", "27.000")]:
            guitar = self._guitar(lo, hi, num_strings=7)
            self.assertTrue(guitar.scale_display.endswith('"'))

    def test_scale_display_persisted_guitar(self):
        # Behaviour is identical for a saved instance (it's a pure property over
        # two columns, but verify there's no save-time surprise).
        guitar = self._guitar("24.750", "24.750")
        guitar.save()
        guitar.refresh_from_db()
        self.assertEqual(guitar.scale_display, '24.75"')

    def test_guitar_str_is_brand_then_name(self):
        # GuitarModel shares the "<brand> <name>" __str__ convention.
        guitar = self._guitar("25.500", "25.500")
        guitar.name = "Custom 24"
        self.assertEqual(str(guitar), "Display Brand Custom 24")

    def test_is_multiscale_flag_matches_display_for_range(self):
        # The display-as-range condition (min != max) is the SAME predicate the
        # derived is_multiscale facet uses, so they must agree.
        single = self._guitar("25.500", "25.500")
        multi = self._guitar("25.500", "27.000", num_strings=7)
        self.assertEqual(single.compute_derived()["is_multiscale"], False)
        self.assertEqual(multi.compute_derived()["is_multiscale"], True)
        self.assertNotIn("–", single.scale_display)
        self.assertIn("–", multi.scale_display)
