# CatalogEntry.status default + queryset helpers + mark_published()
# ---------------------------------------------------------------------------
class CatalogEntryStatusTests(TestCase):
    """status defaults to UNDER_REVISION; published()/under_revision() filter;
    mark_published() flips status and stamps reviewed_at/published_at.

    Brand is the simplest concrete CatalogEntry, so it stands in for the base.
    """

    def test_status_defaults_to_under_revision(self):
        brand = Brand.objects.create(name="Ibanez")
        self.assertEqual(brand.status, PublicationStatus.UNDER_REVISION)
        self.assertEqual(brand.status, "under_revision")

    def test_new_entry_has_no_review_timestamps(self):
        brand = Brand.objects.create(name="Jackson")
        self.assertIsNone(brand.reviewed_at)
        self.assertIsNone(brand.published_at)

    def test_under_revision_queryset_filters_to_pending(self):
        pending = Brand.objects.create(name="Pending Brand")
        published = Brand.objects.create(
            name="Live Brand", status=PublicationStatus.PUBLISHED
        )
        rejected = Brand.objects.create(
            name="Bad Brand", status=PublicationStatus.REJECTED
        )
        result = set(Brand.objects.under_revision())
        self.assertIn(pending, result)
        self.assertNotIn(published, result)
        self.assertNotIn(rejected, result)

    def test_published_queryset_filters_to_published(self):
        pending = Brand.objects.create(name="Pending Brand")
        published = Brand.objects.create(
            name="Live Brand", status=PublicationStatus.PUBLISHED
        )
        rejected = Brand.objects.create(
            name="Bad Brand", status=PublicationStatus.REJECTED
        )
        result = set(Brand.objects.published())
        self.assertIn(published, result)
        self.assertNotIn(pending, result)
        self.assertNotIn(rejected, result)

    def test_published_excludes_rejected(self):
        Brand.objects.create(name="Rejected Co", status=PublicationStatus.REJECTED)
        self.assertEqual(Brand.objects.published().count(), 0)

    def test_published_and_under_revision_are_disjoint(self):
        Brand.objects.create(name="A", status=PublicationStatus.UNDER_REVISION)
        Brand.objects.create(name="B", status=PublicationStatus.PUBLISHED)
        Brand.objects.create(name="C", status=PublicationStatus.REJECTED)
        published_ids = set(Brand.objects.published().values_list("pk", flat=True))
        revision_ids = set(
            Brand.objects.under_revision().values_list("pk", flat=True)
        )
        self.assertEqual(published_ids & revision_ids, set())
        # The rejected row appears in neither bucket.
        self.assertEqual(len(published_ids) + len(revision_ids), 2)

    def test_queryset_helpers_chainable(self):
        # The helpers live on a QuerySet (not just the manager), so they must be
        # chainable after an ordinary filter.
        Brand.objects.create(name="Fender", status=PublicationStatus.PUBLISHED)
        Brand.objects.create(name="Squier", status=PublicationStatus.PUBLISHED)
        qs = Brand.objects.filter(name="Fender").published()
        self.assertEqual(qs.count(), 1)
        self.assertEqual(qs.first().name, "Fender")

    def test_mark_published_sets_status_and_timestamps(self):
        brand = Brand.objects.create(name="Gibson")
        before = timezone.now()
        brand.mark_published()
        after = timezone.now()

        self.assertEqual(brand.status, PublicationStatus.PUBLISHED)
        self.assertIsNotNone(brand.reviewed_at)
        self.assertIsNotNone(brand.published_at)
        # Stamps fall within the call window.
        self.assertGreaterEqual(brand.reviewed_at, before)
        self.assertLessEqual(brand.reviewed_at, after)
        self.assertGreaterEqual(brand.published_at, before)
        self.assertLessEqual(brand.published_at, after)
        # Both stamps come from the same `now`.
        self.assertEqual(brand.reviewed_at, brand.published_at)

    def test_mark_published_persists_to_db_by_default(self):
        brand = Brand.objects.create(name="ESP")
        brand.mark_published()
        reloaded = Brand.objects.get(pk=brand.pk)
        self.assertEqual(reloaded.status, PublicationStatus.PUBLISHED)
        self.assertIsNotNone(reloaded.published_at)
        self.assertIsNotNone(reloaded.reviewed_at)

    def test_mark_published_moves_entry_between_querysets(self):
        brand = Brand.objects.create(name="Schecter")
        self.assertIn(brand, Brand.objects.under_revision())
        self.assertNotIn(brand, Brand.objects.published())

        brand.mark_published()

        self.assertNotIn(brand, Brand.objects.under_revision())
        self.assertIn(brand, Brand.objects.published())

    def test_mark_published_save_false_does_not_touch_db(self):
        brand = Brand.objects.create(name="PRS")
        brand.mark_published(save=False)
        # In-memory object is updated...
        self.assertEqual(brand.status, PublicationStatus.PUBLISHED)
        self.assertIsNotNone(brand.published_at)
        # ...but the DB row is untouched.
        reloaded = Brand.objects.get(pk=brand.pk)
        self.assertEqual(reloaded.status, PublicationStatus.UNDER_REVISION)
        self.assertIsNone(reloaded.published_at)

    def test_mark_published_bumps_updated_at(self):
        brand = Brand.objects.create(name="Charvel")
        original_updated = brand.updated_at
        # Force a measurable gap so auto_now produces a strictly later value.
        Brand.objects.filter(pk=brand.pk).update(
            updated_at=timezone.now() - timedelta(hours=1)
        )
        brand.refresh_from_db()
        stale_updated = brand.updated_at
        self.assertLess(stale_updated, original_updated)

        brand.mark_published()
        brand.refresh_from_db()
        self.assertGreater(brand.updated_at, stale_updated)


# ---------------------------------------------------------------------------
# GearItem.__str__ == "<brand> <name>"
# ---------------------------------------------------------------------------
class GearStrTests(TestCase):
    """Every concrete GearItem renders as "<brand> <name>" (Brand.__str__ is its
    name), across all four gear types."""

    def setUp(self):
        self.brand = Brand.objects.create(
            name="Seymour Duncan", status=PublicationStatus.PUBLISHED
        )

    def test_bridge_str(self):
        bridge_type = BridgeType.objects.create(name="Floyd Rose", is_tremolo=True)
        bridge = Bridge.objects.create(
            brand=self.brand, name="1000 Series", bridge_type=bridge_type
        )
        self.assertEqual(str(bridge), "Seymour Duncan 1000 Series")

    def test_pickup_str(self):
        pickup_type = PickupType.objects.create(name="Humbucker", symbol="H")
        pickup = Pickup.objects.create(
            brand=self.brand, name="JB SH-4", pickup_type=pickup_type
        )
        self.assertEqual(str(pickup), "Seymour Duncan JB SH-4")

    def test_tuner_str(self):
        tuner = Tuner.objects.create(brand=self.brand, name="Locking Tuners")
        self.assertEqual(str(tuner), "Seymour Duncan Locking Tuners")

    def test_nut_str(self):
        nut_material = NutMaterial.objects.create(name="Bone")
        nut = Nut.objects.create(
            brand=self.brand, name="Standard Nut", material=nut_material
        )
        self.assertEqual(str(nut), "Seymour Duncan Standard Nut")

    def test_str_uses_brand_str_not_id(self):
        # The format embeds the Brand object, whose __str__ is its name — so the
        # rendered string must contain the brand NAME, never a pk/repr.
        other_brand = Brand.objects.create(name="DiMarzio")
        pickup_type = PickupType.objects.create(name="Single Coil", symbol="S")
        pickup = Pickup.objects.create(
            brand=other_brand, name="Area 67", pickup_type=pickup_type
        )
        self.assertEqual(str(pickup), "DiMarzio Area 67")
        self.assertNotIn(str(other_brand.pk), str(pickup))

    def test_str_reflects_name_change(self):
        tuner = Tuner.objects.create(brand=self.brand, name="Old Name")
        tuner.name = "New Name"
        self.assertEqual(str(tuner), "Seymour Duncan New Name")


