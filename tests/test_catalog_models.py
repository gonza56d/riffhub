# ---------------------------------------------------------------------------
# ControlledVocabulary.save() slug auto-generation
# ---------------------------------------------------------------------------
class ControlledVocabularySlugTests(TestCase):
    """ControlledVocabulary.save() slugifies `name` into `slug` when blank."""

    def test_slug_autogenerated_from_name_when_blank(self):
        material = FretMaterial.objects.create(name="Stainless Steel")
        self.assertEqual(material.slug, "stainless-steel")

    def test_slug_persisted_to_db(self):
        FretMaterial.objects.create(name="Nickel Silver")
        reloaded = FretMaterial.objects.get(name="Nickel Silver")
        self.assertEqual(reloaded.slug, "nickel-silver")

    def test_explicit_slug_is_not_overwritten(self):
        material = FretMaterial.objects.create(
            name="Evo Gold", slug="custom-evo"
        )
        self.assertEqual(material.slug, "custom-evo")

    def test_slug_strips_punctuation_and_lowercases(self):
        # slugify drops apostrophes/punctuation and lowercases everything.
        shape = BodyShape.objects.create(name="Flying V (Gibson's)")
        self.assertEqual(shape.slug, "flying-v-gibsons")

    def test_slug_collapses_internal_whitespace(self):
        shape = BodyShape.objects.create(name="Super   Strat")
        self.assertEqual(shape.slug, "super-strat")

    def test_slug_truncated_to_120_chars(self):
        # name is max_length=100, slug max_length=120; verify the [:120] guard
        # never produces a slug longer than the column allows.
        long_name = "A" * 100
        material = FretMaterial.objects.create(name=long_name)
        self.assertLessEqual(len(material.slug), 120)
        self.assertEqual(material.slug, "a" * 100)

    def test_slug_regenerated_on_save_when_cleared(self):
        # save() only fills slug when it's falsy, so clearing it then re-saving
        # regenerates from the (possibly new) name.
        material = FretMaterial.objects.create(name="Jumbo")
        self.assertEqual(material.slug, "jumbo")
        material.name = "Medium Jumbo"
        material.slug = ""
        material.save()
        self.assertEqual(material.slug, "medium-jumbo")

    def test_existing_slug_kept_even_when_name_changes(self):
        # If the slug is already set, changing the name must NOT silently
        # re-slug it (URLs stay stable).
        material = FretMaterial.objects.create(name="Jumbo")
        original_slug = material.slug
        material.name = "Extra Jumbo"
        material.save()
        material.refresh_from_db()
        self.assertEqual(material.slug, original_slug)

    def test_default_str_is_name(self):
        country = Country.objects.create(name="Japan")
        self.assertEqual(str(country), "Japan")

    def test_default_ordering_by_name(self):
        FretMaterial.objects.create(name="Zinc")
        FretMaterial.objects.create(name="Brass")
        FretMaterial.objects.create(name="Cobalt")
        names = list(FretMaterial.objects.values_list("name", flat=True))
        self.assertEqual(names, ["Brass", "Cobalt", "Zinc"])

    def test_radius_subclass_also_slugs(self):
        # FretboardRadius adds fields but inherits the slug behaviour.
        radius = FretboardRadius.objects.create(
            name="12 inch",
            radius_min_inches=Decimal("12.00"),
            radius_max_inches=Decimal("12.00"),
        )
        self.assertEqual(radius.slug, "12-inch")


