"""Tests for the catalog domain *models* (catalog/models/).

Focus areas (per assignment):
- ControlledVocabulary.save() auto-generates a slug from name.
- CatalogEntry.status defaults to UNDER_REVISION; the .published()/.under_revision()
  queryset helpers filter correctly; mark_published() flips status + stamps times.
- GearItem.__str__ == "<brand> <name>".
- GuitarModel.scale_display trims trailing zeros (single value vs multiscale range).

These exercise the abstract bases through concrete subclasses (Brand, FretMaterial,
Bridge, GuitarModel, ...) since the bases themselves are abstract.
"""

